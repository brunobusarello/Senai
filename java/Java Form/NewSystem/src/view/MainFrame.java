    public MainFrame() {
        initComponents();
        scheduler.scheduleAtFixedRate(() -> {
            try {
                if (connection.connection()) {
                    jMenuItem1.setEnabled(true);
                    jMenuItem2.setEnabled(true);
                    jMenuItem3.setEnabled(true);
                    lblConnection.setText("Conexão bem-sucedida");
                    imgConnection.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/banco-conectado.png")));
                }
                else{
                    jMenuItem1.setEnabled(false);
                    jMenuItem2.setEnabled(false);
                    jMenuItem3.setEnabled(false);
                    lblConnection.setText("Conexão má-sucedida");
                    imgConnection.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/banco-desconectado.png")));
                }
            } catch (Exception e) {
                System.out.println("Erro ao testar conexão");
            }
        }, 0, 1, TimeUnit.SECONDS);
    }

